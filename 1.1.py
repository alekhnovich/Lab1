leftBorder = int(input("Введите число, с которого начинается диапазон "))
rightBorder = int(input("Введите число, которым заканчивается диапазон "))
if leftBorder > rightBorder:
    print("Введите правильный диапазон!")
else:
    for i in range(leftBorder + 1, rightBorder):
        if i > 0:
            print(i)
            i = i + 1
print("Были выведены все простые числа из заданного даиапазона!")
